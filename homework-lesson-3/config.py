from pydantic_settings import BaseSettings, SettingsConfigDict

class Settings(BaseSettings):
    GOOGLE_API_KEY: str = ""
    OPENAI_API_KEY: str = "" # Для прикладу зміни провайдера
    
    # Налаштування Context Engineering
    MAX_SEARCH_RESULTS: int = 5
